        val LocationId: String?,
        val Locked: Boolean?,
        val MaxAttendees: Int?,
        val MinAttendees: Int?,
        val Name: String?,
        val NotesComments: String?,
        val NotifyBy: String?,
        val NotifyPeriod: Int?,
        val ParentId: String?,
        val Quantity: Int?,
        val RecurringScheduleId: String?,
        val RegionId: String?,
        val ScheduleTemplateId: String?,
        val Start: String?,
        val TemplatedJobId: String?,
        val Type: String?,
        val Urgency: String?,
        val VirtualMeetingId: String?,
        val VirtualMeetingInfo: String?,
        val VirtualMeetingURL: String?,
        override val CustomFields: Map<String, Any?>?,
    ) : EventModel(CustomFields)

    data class JobAllocationsEventModel(
        val UID: String? = null,
        val DeclineDescription: String?,
        val DeclineReason: String?,
        val Duration: Int?,
        val End: String?,
        val EstimatedTravelDistance: Double?,
        val EstimatedTravelTime: Int?,
        val GeoCheckedInLatitude: Double?,
        val GeoCheckedInLongitude: Double?,
        val GeoCompletedLatitude: Double?,
        val GeoCompletedLongitude: Double?,
