        val UID: String? = null,
        val Address: String?,
        val CopiedFromId: String?,
        val End: String?,
        val GeoLatitude: Double?,
        val GeoLongitude: Double?,
        val LocationId: String?,
        val Notes: String?,
        val Quantity: Int?,
        val ResourceId: String?,
        val ScheduleTemplateId: String?,
        val Start: String?,
        val TemplatedActivityId: String?,
        val Timezone: String?,
        val Type: String?,
