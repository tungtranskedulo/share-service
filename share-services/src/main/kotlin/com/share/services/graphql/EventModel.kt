        val Finish: String?,
        val Status: String?,
        val IsAvailable: Boolean?,
        val Type: String?,
        val Notes: String?,
        override val CustomFields: Map<String, Any?>?,
    ) : EventModel(CustomFields)

    data class JobEventModel(
        val UID: String? = null,
        val AbortReason: String?,
        val AccountId: String?,
        val ActualEnd: String?,
        val ActualStart: String?,
        val Address: String?,
        val AutoSchedule: Boolean?,
        val CanBeDeclined: Boolean?,
        val CompletionNotes: String?,
        val ContactId: String?,
        val CopiedFromId: String?,
        val CustomerConfirmationStatus: String?,
