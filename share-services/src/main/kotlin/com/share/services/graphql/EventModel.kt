        val NotificationType: String?,
        val PhoneResponse: String?,
        val ResourceId: String?,
        val ResourceRequirementId: String?,
        val Start: String?,
        val Status: String?,
        val TeamLeader: Boolean?,
        val TimeCheckedIn: String?,
        val TimeCompleted: String?,
        val TimeInProgress: String?,
        val TimePublished: String?,
        val TimeResponded: String?,
        val TimeStartTravel: String?,
        val TravelDistance: Double?,
        val TravelTime: Int?,
        override val CustomFields: Map<String, Any?>?,
    ) : EventModel(CustomFields)

    data class AccountsEventModel(
        val UID: String? = null,
        val BillingCity: String?,
        val BillingPostalCode: String?,
        val BillingState: String?,
        val BillingStreet: String?,
        val Fax: String?,
        val Name: String?,
        val Phone: String?,
        val Rank: Int?,
        val RequiresWhitelist: Boolean?,
        val ShippingCity: String?,
        val ShippingPostalCode: String?,
        val ShippingState: String?,
        val ShippingStreet: String?,
